        import_boundary_data = self._load_tool_data(
            "analyze_dev_tools_import_boundaries", "imports"
        )
        if import_boundary_data and isinstance(import_boundary_data, dict):
            ib_summary = import_boundary_data.get("summary", {}) or {}
